package com.testinium.testiniumAssesment.dto;

public class BookStoreDto {

    private Integer id;
    private String name;
    private String city;

    public Integer getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
